type TranslationFn = (key: TranslationKey) => string;

const translations: Record<Locale, Translations> = { en, et };

const STORAGE_KEY = "or.portal.locale";

const LanguageContext = createContext<{
  locale: Locale;
  setLocale: (locale: Locale) => void;
  t: TranslationFn;
}>({
  locale: "en",
  setLocale: () => {},
  t: (key) => en[key],
});

export function LanguageProvider({ children }: { children: React.ReactNode }) {
  const [locale, setLocaleState] = useState<Locale>("en");

  useEffect(() => {
    const stored = localStorage.getItem(STORAGE_KEY);
    if (stored === "et" || stored === "en") {
      setLocaleState(stored);
    }
  }, []);

  const setLocale = useCallback((next: Locale) => {
    setLocaleState(next);
    localStorage.setItem(STORAGE_KEY, next);
  }, []);

  const tFn = useCallback<TranslationFn>(
    (key) => translations[locale][key],
    [locale],
  );

  return React.createElement(
    LanguageContext.Provider,
    { value: { locale, setLocale, t: tFn } },
    children,
  );
}

export function useT(): TranslationFn {
  return useContext(LanguageContext).t;
}

export function useLocale() {
  const { locale, setLocale } = useContext(LanguageContext);
  return { locale, setLocale };
}
